//EN ESTE ARCHIVO SE ENCUENTRAS LOS DOXYGEM Y LOS PROTOTIPOS DE LAS FUNCIONES

/** \brief suma de 2 numeros
 *
 * \param (A+B)
 * \return resultado suma
 *
 */
int sumar(int A, int B);

/** \brief resta de 2 numeros
 *
 * \param (A-B)
 * \return resultado resta
 *
 */
int restar(int A, int B);

/** \brief division de 2 numeros
 *
 * \param (A/B)
 * \return resultado division
 *
 */
float dividir(float A, float B);

/** \brief multiplicacion de 2 numeros
 *
 * \param (A*B)
 * \return resultado multiplicacion
 *
 */
int multiplicar(int A, int B);

/** \brief factorizacion de un numero
 *
 * \param (A*(A-1)*(A-2),ETC..)
 * \return resultado factorizacion
 *
 */
int factorizar(int operando);
